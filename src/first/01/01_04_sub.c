#include <stdio.h>

int main(int argc, const char *argv[]) {
    // a=5, b=3を定義し引き算する
    int a = 5;
    int b = 3;
    int difference = a - b;
    // 引き算の結果を表示する
    printf("%d - %d = %d\n", a, b, difference);
    
    return 0;
}

